#ifndef _VQTYPES_H
#define _VQTYPES_H

// Primitive types
typedef char               vq_i8_t;
typedef unsigned char      vq_u8_t;
typedef short              vq_i16_t;
typedef unsigned short     vq_u16_t;
typedef int                vq_i32_t;
typedef unsigned int       vq_u32_t;
typedef long long          vq_i64_t;
typedef unsigned long long vq_u64_t;

#endif
 