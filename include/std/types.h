// Shortcut types; Typedefs around integers

typedef signed char std_byte_t;
typedef unsigned char std_ubyte_t;

typedef signed short std_word_t;
typedef unsigned short std_uword_t;

typedef signed int std_dword_t;
typedef unsigned int std_udword_t;

typedef signed long long std_qword_t;
typedef unsigned long long std_uqword_t;

#endif
